    FUNCTION CO_CONDUCTORES RETURN SYS_REFCURSOR;
    FUNCTION CO_VEHICULOS RETURN SYS_REFCURSOR;
    FUNCTION CO_ACCIDENTES RETURN SYS_REFCURSOR;
